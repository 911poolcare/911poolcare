/**
 * Pull Google reviews via Places API (New) and write content/generated/google-reviews.ts.
 *
 * Requires:
 *   GOOGLE_PLACES_API_KEY
 *   GOOGLE_PLACE_ID  (find in Google Maps → Share → Embed → look for place_id=...)
 *
 * Usage: npm run import:reviews
 *
 * Note: Google returns up to 5 reviews per request. Curate additional highlights in
 * content/testimonials.ts or re-run after new reviews post.
 */
import { mkdir, writeFile } from "fs/promises";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const outPath = join(root, "content", "generated", "google-reviews.ts");

const apiKey = process.env.GOOGLE_PLACES_API_KEY;
const placeId = process.env.GOOGLE_PLACE_ID;
const minRating = Number(process.env.MIN_REVIEW_RATING ?? "4");

if (!apiKey || !placeId) {
  console.error(
    "Set GOOGLE_PLACES_API_KEY and GOOGLE_PLACE_ID to import Google reviews.",
  );
  console.error("Reviews remain curated in content/testimonials.ts until then.");
  process.exit(1);
}

const url = `https://places.googleapis.com/v1/places/${placeId}`;

const response = await fetch(url, {
  headers: {
    "Content-Type": "application/json",
    "X-Goog-Api-Key": apiKey,
    "X-Goog-FieldMask": "reviews,rating,userRatingCount",
  },
});

if (!response.ok) {
  console.error("Places API error:", response.status, await response.text());
  process.exit(1);
}

const data = await response.json();
const reviews = (data.reviews ?? [])
  .map((review) => ({
    name: review.authorAttribution?.displayName ?? "Google reviewer",
    quote: review.text?.text ?? "",
    rating: review.rating ?? 5,
    datePublished: review.publishTime?.slice(0, 10),
  }))
  .filter((review) => review.quote && review.rating >= minRating);

await mkdir(dirname(outPath), { recursive: true });

const file = `/** Auto-generated by scripts/import-google-reviews.mjs — do not edit by hand */
import type { GoogleReview } from "@/content/testimonials";

export const importedGoogleReviews: GoogleReview[] = ${JSON.stringify(reviews, null, 2)} as const;

export const importedGoogleStats = {
  rating: ${data.rating ?? "null"},
  reviewCount: ${data.userRatingCount ?? "null"},
} as const;
`;

await writeFile(outPath, file, "utf8");
console.log(`Wrote ${reviews.length} reviews to ${outPath}`);
