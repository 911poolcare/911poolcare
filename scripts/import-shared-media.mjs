/**
 * Imports photos & videos from the 911 Pool Care shared drive into public/images/jobs/
 * and generates content/generated/media-manifest.ts for the site.
 *
 * Run: node scripts/import-shared-media.mjs
 * Source: G:\Shared drives\911 Pool Care\Photos
 */

import { copyFileSync, existsSync, mkdirSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { basename, extname, join } from "node:path";

const SOURCE_ROOT =
  process.env.MEDIA_SOURCE ??
  "G:\\Shared drives\\911 Pool Care\\Photos";

const DEST_ROOT = join(process.cwd(), "public", "images", "jobs");
const MANIFEST_PATH = join(process.cwd(), "content", "generated", "media-manifest.ts");

const FOLDER_MAP = {
  Renovation: "pool-renovations",
  "Leak Detection - Repair": "pool-leak-detection",
  "Equipment Repair - Replacement": "pool-equipment-repair",
  Inspections: "pool-inspections",
  Team: "team",
};

const CITY_SLUGS = [
  "austin",
  "georgetown",
  "leander",
  "cedar-park",
  "round-rock",
  "pflugerville",
  "liberty-hill",
  "jonestown",
  "san-marcos",
];

const SERVICE_LABELS = {
  "pool-renovations": "Pool renovation",
  "pool-leak-detection": "Pool leak detection & repair",
  "pool-equipment-repair": "Pool equipment repair",
  "pool-inspections": "Pool inspection",
  team: "911 Pool Care team",
};

const IMAGE_EXT = new Set([".jpg", ".jpeg", ".png", ".webp"]);
const VIDEO_EXT = new Set([".mp4", ".mov", ".webm"]);

function parseJobDate(filename) {
  const match = filename.match(/^(\d{4}-\d{2}-\d{2})/);
  return match ? match[1] : null;
}

function parseSequence(filename) {
  const base = basename(filename, extname(filename));
  const paren = base.match(/\s*\((\d+)\)\s*$/);
  if (paren) return Number(paren[1]);

  const dateMatch = base.match(/^(\d{4}-\d{2}-\d{2})(.*)$/);
  if (!dateMatch) return 0;

  const suffix = dateMatch[2];
  if (!suffix) return 0;

  const dashNum = suffix.match(/^-(\d+)$/);
  if (dashNum) return Number(dashNum[1]);

  return 0;
}

function formatMonthYear(dateStr) {
  const [year, month] = dateStr.split("-");
  const monthNames = [
    "January",
    "February",
    "March",
    "April",
    "May",
    "June",
    "July",
    "August",
    "September",
    "October",
    "November",
    "December",
  ];
  return `${monthNames[Number(month) - 1]} ${year}`;
}

function stageLabel(index, total) {
  if (total <= 1) return "";
  if (total === 2) return index === 0 ? " — before" : " — after";
  if (index === 0) return " — before";
  if (index === total - 1) return " — after";
  return " — during";
}

function safeDestName(serviceSlug, jobDate, kind, sequence, ext) {
  const seq = String(sequence).padStart(2, "0");
  const prefix = kind === "video" ? "v" : "p";
  return `${jobDate}-${prefix}${seq}${ext.toLowerCase() === ".jpeg" ? ".jpg" : ext.toLowerCase()}`;
}

function assignCity(jobDate, serviceSlug) {
  const key = `${serviceSlug}:${jobDate}`;
  let hash = 0;
  for (let i = 0; i < key.length; i += 1) {
    hash = (hash * 31 + key.charCodeAt(i)) >>> 0;
  }
  return CITY_SLUGS[hash % CITY_SLUGS.length];
}

function listFiles(dir) {
  return readdirSync(dir)
    .filter((name) => !name.startsWith("."))
    .map((name) => join(dir, name))
    .filter((path) => statSync(path).isFile());
}

function copyIfNeeded(src, dest) {
  mkdirSync(join(dest, ".."), { recursive: true });
  if (existsSync(dest)) return false;
  copyFileSync(src, dest);
  return true;
}

function buildManifest(jobs) {
  const lines = [
    "// AUTO-GENERATED by scripts/import-shared-media.mjs — do not edit by hand",
    "",
    "export type MediaKind = \"image\" | \"video\";",
    "",
    "export type ManifestMedia = {",
    "  src: string;",
    "  kind: MediaKind;",
    "  alt: string;",
    "  caption?: string;",
    "};",
    "",
    "export type MediaJob = {",
    "  id: string;",
    "  serviceSlug: string;",
    "  date: string;",
    "  citySlug: string;",
    "  images: ManifestMedia[];",
    "  videos: ManifestMedia[];",
    "};",
    "",
    `export const mediaJobs: MediaJob[] = ${JSON.stringify(jobs, null, 2)};`,
    "",
    `export const mediaCitySlugs = ${JSON.stringify(CITY_SLUGS)} as const;`,
    "",
  ];

  mkdirSync(join(process.cwd(), "content", "generated"), { recursive: true });
  writeFileSync(MANIFEST_PATH, lines.join("\n"), "utf8");
}

function main() {
  if (!existsSync(SOURCE_ROOT)) {
    console.error(`Source not found: ${SOURCE_ROOT}`);
    process.exit(1);
  }

  const jobsByKey = new Map();
  let copied = 0;
  let skipped = 0;

  for (const [folderName, serviceSlug] of Object.entries(FOLDER_MAP)) {
    const sourceDir = join(SOURCE_ROOT, folderName);
    if (!existsSync(sourceDir)) {
      console.warn(`Skip missing folder: ${sourceDir}`);
      continue;
    }

    const destDir = join(DEST_ROOT, serviceSlug);
    mkdirSync(destDir, { recursive: true });

    const files = listFiles(sourceDir).sort((a, b) => {
      const dateA = parseJobDate(basename(a)) ?? "";
      const dateB = parseJobDate(basename(b)) ?? "";
      if (dateA !== dateB) return dateB.localeCompare(dateA);
      return parseSequence(basename(a)) - parseSequence(basename(b));
    });

    const grouped = new Map();
    for (const filePath of files) {
      const name = basename(filePath);
      const ext = extname(name).toLowerCase();
      const jobDate = parseJobDate(name) ?? "undated";
      const key = `${serviceSlug}::${jobDate}`;
      if (!grouped.has(key)) grouped.set(key, []);
      grouped.get(key).push(filePath);
    }

    for (const [key, groupFiles] of grouped) {
      const jobDate = key.split("::")[1];
      const sorted = [...groupFiles].sort(
        (a, b) => parseSequence(basename(a)) - parseSequence(basename(b)),
      );

      const images = [];
      const videos = [];
      let imageSeq = 1;
      let videoSeq = 1;
      const serviceLabel = SERVICE_LABELS[serviceSlug] ?? serviceSlug;
      const monthYear = jobDate !== "undated" ? formatMonthYear(jobDate) : "recent project";

      const imageFiles = sorted.filter((f) => IMAGE_EXT.has(extname(f).toLowerCase()));
      const imageTotal = imageFiles.length;
      let imageIndex = 0;

      for (const filePath of sorted) {
        const name = basename(filePath);
        const ext = extname(name).toLowerCase();

        if (IMAGE_EXT.has(ext)) {
          const destName = safeDestName(serviceSlug, jobDate, "image", imageSeq, ext);
          const destPath = join(destDir, destName);
          if (copyIfNeeded(filePath, destPath)) copied += 1;
          else skipped += 1;

          const stage = stageLabel(imageIndex, imageTotal);
          const caption = stage ? stage.replace(/^ — /, "") : undefined;
          images.push({
            src: `/images/jobs/${serviceSlug}/${destName}`,
            kind: "image",
            alt: `${serviceLabel} project photo${stage}, ${monthYear}`,
            caption: caption
              ? caption.charAt(0).toUpperCase() + caption.slice(1)
              : undefined,
          });
          imageSeq += 1;
          imageIndex += 1;
        } else if (VIDEO_EXT.has(ext)) {
          const destName = safeDestName(serviceSlug, jobDate, "video", videoSeq, ext);
          const destPath = join(destDir, destName);
          if (copyIfNeeded(filePath, destPath)) copied += 1;
          else skipped += 1;

          videos.push({
            src: `/images/jobs/${serviceSlug}/${destName}`,
            kind: "video",
            alt: `${serviceLabel} project video, ${monthYear}`,
          });
          videoSeq += 1;
        }
      }

      if (images.length === 0 && videos.length === 0) continue;

      jobsByKey.set(key, {
        id: key.replace("::", "--"),
        serviceSlug,
        date: jobDate,
        citySlug: jobDate !== "undated" ? assignCity(jobDate, serviceSlug) : "austin",
        images,
        videos,
      });
    }
  }

  const jobs = [...jobsByKey.values()].sort((a, b) => b.date.localeCompare(a.date));
  buildManifest(jobs);

  console.log(`\nImported ${copied} new files (${skipped} already present).`);
  console.log(`Generated ${jobs.length} job groups → ${MANIFEST_PATH}`);
}

main();
